package com.myParking.model;

public class ParkingArea{

	private int id;

	private Vehicle vehicle;

	public ParkingArea() {

	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public Vehicle getVehicle() {
		return vehicle;
	}

	public void setVehicle(Vehicle vehicle) {
		this.vehicle = vehicle;
	}


}
